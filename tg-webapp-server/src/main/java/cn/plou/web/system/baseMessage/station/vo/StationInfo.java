package cn.plou.web.system.baseMessage.station.vo;

import cn.plou.web.system.baseMessage.station.entity.Station;
import com.fasterxml.jackson.annotation.JsonInclude;

import java.io.Serializable;

@JsonInclude(JsonInclude.Include.NON_NULL)
public class StationInfo extends Station implements Serializable {
    private static final long serialVersionUID = 123456789L;
    private String companyName;
    private String producerName;
    private String pipeTypeName;
    private String stationTypeName;
    private String departmentName;
    private String staffName;
    private String businessTypeName;

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getProducerName() {
        return producerName;
    }

    public void setProducerName(String producerName) {
        this.producerName = producerName;
    }

    public String getPipeTypeName() {
        return pipeTypeName;
    }

    public void setPipeTypeName(String pipeTypeName) {
        this.pipeTypeName = pipeTypeName;
    }

    public String getStationTypeName() {
        return stationTypeName;
    }

    public void setStationTypeName(String stationTypeName) {
        this.stationTypeName = stationTypeName;
    }

    public String getDepartmentName() {
        return departmentName;
    }

    public void setDepartmentName(String departmentName) {
        this.departmentName = departmentName;
    }

    public String getStaffName() {
        return staffName;
    }

    public void setStaffName(String staffName) {
        this.staffName = staffName;
    }

    public String getBusinessTypeName() {
        return businessTypeName;
    }

    public void setBusinessTypeName(String businessTypeName) {
        this.businessTypeName = businessTypeName;
    }
}
