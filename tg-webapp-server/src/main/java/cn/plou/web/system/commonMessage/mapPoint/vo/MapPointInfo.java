package cn.plou.web.system.commonMessage.mapPoint.vo;

import cn.plou.web.system.commonMessage.mapPoint.entity.MapPoint;
import com.fasterxml.jackson.annotation.JsonInclude;

@JsonInclude(JsonInclude.Include.NON_NULL)
public class MapPointInfo extends MapPoint {

    private String companyName;

    private String meterTypeName;

    private String stationName;

    private String colorName;

    private String commuityName;

    private String mapPointType;

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getStationName() {
        return stationName;
    }

    public void setStationName(String stationName) {
        this.stationName = stationName;
    }

    public String getColorName() {
        return colorName;
    }

    public void setColorName(String colorName) {
        this.colorName = colorName;
    }

    public String getCommuityName() {
        return commuityName;
    }

    public void setCommuityName(String commuityName) {
        this.commuityName = commuityName;
    }

    public String getMeterTypeName() {
        return meterTypeName;
    }

    public void setMeterTypeName(String meterTypeName) {
        this.meterTypeName = meterTypeName;
    }

    public String getMapPointType() {
        return mapPointType;
    }

    public void setMapPointType(String mapPointType) {
        this.mapPointType = mapPointType;
    }
}
