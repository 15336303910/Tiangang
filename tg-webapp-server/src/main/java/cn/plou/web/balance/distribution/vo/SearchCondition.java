package cn.plou.web.balance.distribution.vo;

import java.math.BigDecimal;

public class SearchCondition {
    private BigDecimal flowingDeviation;
    private BigDecimal temperatureDeviation;
    private BigDecimal pressureDeviation;
    private String heatingIndex;
    private String flowingIndex;

    public BigDecimal getFlowingDeviation() {
        return flowingDeviation;
    }

    public void setFlowingDeviation(BigDecimal flowingDeviation) {
        this.flowingDeviation = flowingDeviation;
    }

    public BigDecimal getTemperatureDeviation() {
        return temperatureDeviation;
    }

    public void setTemperatureDeviation(BigDecimal temperatureDeviation) {
        this.temperatureDeviation = temperatureDeviation;
    }

    public BigDecimal getPressureDeviation() {
        return pressureDeviation;
    }

    public void setPressureDeviation(BigDecimal pressureDeviation) {
        this.pressureDeviation = pressureDeviation;
    }

    public String getHeatingIndex() {
        return heatingIndex;
    }

    public void setHeatingIndex(String heatingIndex) {
        this.heatingIndex = heatingIndex;
    }

    public String getFlowingIndex() {
        return flowingIndex;
    }

    public void setFlowingIndex(String flowingIndex) {
        this.flowingIndex = flowingIndex;
    }
}
